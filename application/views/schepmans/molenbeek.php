<h2 class="molenbeek_title"> Gare aux cambrioleurs
</h2>
    <p class="p_molenbeek">

Molenbeek n’est pas davantage touché par le problème des cambriolages dans les habitations que

d’autres communes bruxelloises mais, malgré tout, il est important de prendre des mesures de

prévention. Récemment, la commune a organisé une réunion avec les habitants afin de leur

présenter le service communal de prévention contre les cambriolages. Plus d’informations dans cette

vidéo : <a href="https://www.facebook.com/ilikemolenbeek/videos/1673497959347867 ">https://www.facebook.com/ilikemolenbeek/videos/1673497959347867</a>
