    <h2 class="molenbeek_title">83 logements sociaux rénovés !</h2>
    <p class="p_molenbeek">
        La Cour Saint-Lazare, le complexe de logements sociaux à Molenbeek-Saint-Jean, a été complètement rénovée par Beliris. Après
        un peu plus de deux ans de travaux, les deux bâtiments qui la composent, sont prêtes à accueillir leurs nouveaux
        habitants.
    </p>

    <p class="p_molenbeek">Grâce à la rénovation des bâtiments de la Cour Saint-Lazare, Le Logement Molenbeekois bénéficie de 83 logements totalement
        rénovés (au lieu des 180 appartements exigus comme précédemment). Ils offrent de meilleures conditions de vie pour
        les familles. Outre la qualité des appartements, les équipements communs comme la salle polyvalente et toutes les
        installations techniques ont été totalement repensés. La cour intérieure est réaménagée et réservée aux habitants,
        tant aux enfants qu’aux adultes. Les économies d’énergie jouent un rôle important dans ce projet. Les façades et
        les toitures ont été intégralement isolées. Un système de ventilation avec récupération de chaleur a été installé.</p>

    <p class="p_molenbeek">Le rénovation du complexe Cour Saint-Lazare, construit en 1927 par le célèbre architecte Joseph Diongre, renoue avec
        le projet original de l’architecte. Les balustrades, par exemple, qui donnent au site son cachet, s’inspirent du
        modèle original.</p>

    <p class="p_molenbeek">«La Cour Saint-Lazare, c’est tout un pan de notre histoire patrimoniale, architecturale qui bénéficie aujourd’hui d’une
        rénovation en profondeur », a expliqué la bourgmestre Françoise Schepmans, lors de l’inauguration réunissant la ministre
        bruxelloise du Logement, Céline Fremault, le président du Logement Molenbeekois, Michel Eylenbosch, et les édiles
        de la commune.</p>
