    <h2 class="molenbeek_title">  Au service des habitants !</h2>
    <p class="p_molenbeek">
  


Afin d’accueillir les citoyens dans de bonnes conditions, un nouveau bâtiment administratif a été

construit rue Sainte-Marie, à proximité de la Maison communale et de la station Comte de Flandre.

Impossible de le manquer : le nom de Molenbeek est affiché en grandes lettres sur sa façade.

Disposé sur deux étages, ce bâtiment contient tous les guichets des services Population, Etat civil et

Etrangers. L’architecte Blondel a veillé à concevoir un espace spacieux et éclairé qui changent

radicalement des anciens guichets vétustes de la rue du Niveau.

Par ailleurs, une antenne de proximité ouvrira prochainement ses portes rue Charles Malis. Les

habitants du quartier pourront y effectuer les démarches administratives. «Ce sera une grande

facilité notamment pour les personnes âgées», souligne Françoise Schepmans.
    </p>

