    <h2 class="molenbeek_title">Deux nouvelles écoles secondaires à Molenbeek en 2017 !</h2>
    <p class="p_molenbeek">

Pour le groupe libéral, l’éducation de nos jeunes est primordiale afin qu’ils puissent

s’épanouir et se préparer à l’avenir avec les meilleurs atouts. Il faut répondre à cette priorité

avec rapidité car le boom démographique, jusqu’ici ressenti dans le réseau primaire, se

répercutera rapidement sur l’enseignement secondaire.

C’est donc avec beaucoup d’enthousiasme que Molenbeek s’inscrit dans un projet de création

de deux nouvelles écoles sur son territoire : l’une sera installée chaussée de Gand sur le site de

l’entreprise Takeda dans le quartier Karreveld, l’autre à l’avenue Jean Dubrucq dans le

quartier Maritime.

</p>
<p class="p_molenbeek">

Ces deux écoles seront ouvertes dès la rentrée de septembre 2017 ! Sous l’autorité d’un

pouvoir organisateur qui réunit la Fédération Wallonie-Bruxelles, les communes de

Molenbeek-Saint- Jean et de Berchem-Sainte- Agathe, un collectif d’enseignant l’asbl « Ecole

ensemble » et l’ULB, ces deux établissements scolaires seront de qualité, avec une pédagogie

active, et favoriseront la mixité.

« Trop souvent, je rencontre des parents qui sont contraints d’envoyer leurs enfants dans des

écoles parfois fort loin de leur domicile à Molenbeek », explique Françoise Schepmans en

charge de l’Instruction publique francophone. «Ces deux nouveaux établissements

secondaires seront donc une très belle opportunité pour soulager les parents à la recherche

d’école de qualité ».

Plusieurs permanences ont été organisées à l’attention des élèves de sixième primaire qui

passeront en secondaire l’an prochain. Les inscriptions sont soumises au décret de la

Fédération Wallonie Bruxelles. Infos : <a href="http://www.popluriel.be">www.popluriel.be</a> </p> link

