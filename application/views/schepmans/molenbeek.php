    <h2 class="molenbeek_title">Une véritable réforme fiscale qui profite à tous</h2>
    <img class="img_molenbeek" src="<?= base_url("assets/images/tirelire-euro.jpg ")?>" alt="">
    <p class="p_molenbeek">Pour stimuler l’économie et soutenir les classes moyennes, le MR entend poursuivre les réformes clés qui ont été menées
        cette décennie. Pour ce faire, sont notamment visées la réduction au niveau de la moyenne européenne de la fiscalité
        sur le travail des citoyens et de l’impôt des sociétés, l’exonération sur les droits de succession et de donation,
        la suppression de la taxe régionale pour les familles et les entreprises.</p>
    <p class="p_molenbeek">Pour compenser ce retour d’impôts aux contribuables, il sera notamment mis en oeuvre un plan d’économie de certaines
        dépenses inutiles des pouvoirs publics. Plus de justice fiscale pour les classes moyennes et les PME, c’est notre
        engagement.</p>

